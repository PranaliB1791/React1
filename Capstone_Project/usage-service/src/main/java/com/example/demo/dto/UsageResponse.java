package com.example.demo.dto;

import java.time.LocalDateTime;

public class UsageResponse {
	private String id;
	private Long userId;
	private Long planId;
	private int callMinutes;
	private double dataUsageInGB;
	private int smsCount;
	private LocalDateTime timestamp;

	public UsageResponse() {
	}

	public UsageResponse(String id, Long userId, Long planId, int callMinutes, double dataUsageInGB, int smsCount,
			LocalDateTime timestamp) {
		super();
		this.id = id;
		this.userId = userId;
		this.planId = planId;
		this.callMinutes = callMinutes;
		this.dataUsageInGB = dataUsageInGB;
		this.smsCount = smsCount;
		this.timestamp = timestamp;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public Long getUserId() {
		return userId;
	}

	public void setUserId(Long userId) {
		this.userId = userId;
	}

	public Long getPlanId() {
		return planId;
	}

	public void setPlanId(Long planId) {
		this.planId = planId;
	}

	public int getCallMinutes() {
		return callMinutes;
	}

	public void setCallMinutes(int callMinutes) {
		this.callMinutes = callMinutes;
	}

	public double getDataUsageInGB() {
		return dataUsageInGB;
	}

	public void setDataUsageInGB(double dataUsageInGB) {
		this.dataUsageInGB = dataUsageInGB;
	}

	public int getSmsCount() {
		return smsCount;
	}

	public void setSmsCount(int smsCount) {
		this.smsCount = smsCount;
	}

	public LocalDateTime getTimestamp() {
		return timestamp;
	}

	public void setTimestamp(LocalDateTime timestamp) {
		this.timestamp = timestamp;
	}

	// Getters and setters

}
